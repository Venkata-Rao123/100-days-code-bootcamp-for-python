# learn about the primitive data types in Python
# 1.Strings
# 2.Integers
# 3.Floats
# 4.Booleans
# 1.strings
print("Hello"[0])  # first character of the  accessing string
print("Hello"[4])  # last character of the accessing string
print("Hello"[-1])  # last character of the accessing string
print("123" + "345") # string concatenation

# 2.Integers
print(123 + 345) # integer addition
# large integers
print(123_456_789) # integer with underscores for better readability
# 3.Floats
print(3.14159) # float representation of pi
# 4.Booleans
print(True) # boolean value True
print(False) # boolean value False



