# concept of Variable names
# 1. Variable names can only contain letters, numbers, and underscores.
# 2. Variable names cannot start with a number.
# 3. Variable names are case-sensitive (age, Age and AGE are three different variables).
# 4. Variable names cannot be a reserved word in Python (like print, input, str, etc.).
# 5. Variable names should be descriptive and meaningful.
# 6. Variable names cannot contain spaces.
