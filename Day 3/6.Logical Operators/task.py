# Logical Operators
# A and B are two boolean varaibles are True AND That condition is TRUE
# A or B are two boolean varaibles A OR B  one condition is TRUE other is FALSE then the condition is TRUE
# NOT A is the negation of A, if A is True then NOT A is False


a = 12
b = 5
print(a > 10 and b < 10) # True
print(a > 10 or b > 10) # True
print(not(a > 10 and b < 10)) # False