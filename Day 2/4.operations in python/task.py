# some basics mathematical operations +.-.*,/
print(12 + 23) # addition
print(10 - 5) # subtraction
print(3 * 4) # multiplication
print( 2 / 2) # division
print(2 // 2) # floor division
print(2 ** 2) # exponentiation
print(10 % 3) # modulus

# PEMDAS - Parentheses, Exponents, Multiplication, Division, Addition, Subtraction
print(3 * 3 + 3 / 3 - 3) # this will print 7.0 because of PEMDAS rule
print(3 * (3 + 3) / 3 - 3) # this will print 3.0 because of PEMDAS rule and parentheses

